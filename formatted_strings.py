#If you want to print "Dhanush [Kottapalli] is a coder"
#We can do it in two types:-
#String Concatenation.
#Formatted Strings.
first="Dhanush"
last="Kottapalli"
message = first+" ["+last+"] is a coder"   #String Concatenation.
print(message)
print(first+" ["+last+"] is a coder")      #String Concatenation.

msg= f"{first}[{last}] is a coder"         #Formatted Strings.
print(msg)
print(f"{first}[{last}] is a coder")       #Formatted Strings.
